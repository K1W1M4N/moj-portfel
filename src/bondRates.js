// ─── Pobierz stawkę dla danego typu i daty zakupu ─────────────────────────────
export function getRateForPurchase(bondType, purchaseDate) {
  const rates = BOND_RATES_HISTORY[bondType];
  if (!rates) return null;

  const d = new Date(purchaseDate);
  const yearMonth = `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, "0")}`;

  if (rates[yearMonth]) return rates[yearMonth];

  // Cofaj miesiącami max 3 (zabezpieczenie)
  for (let i = 1; i <= 3; i++) {
    let m = d.getMonth() + 1 - i;
    let y = d.getFullYear();
    if (m <= 0) { m += 12; y -= 1; }
    const key = `${y}-${String(m).padStart(2, "0")}`;
    if (rates[key]) return rates[key];
  }
  return null;
}

// ─── Cache aktualizacji z GitHub Actions ─────────────────────────────────────
const CACHE_KEY = "bond_rates_update";
const CACHE_TTL = 24 * 60 * 60 * 1000;

export async function fetchLatestRates() {
  try {
    const cached = localStorage.getItem(CACHE_KEY);
    if (cached) {
      const { ts, rates } = JSON.parse(cached);
      if (Date.now() - ts < CACHE_TTL) {
        Object.entries(rates).forEach(([type, months]) => {
          if (BOND_RATES_HISTORY[type]) Object.assign(BOND_RATES_HISTORY[type], months);
        });
        return;
      }
    }
  } catch (e) {}

  try {
    const res = await fetch("/api/bond-rates");
    if (!res.ok) return;
    const { rates } = await res.json();
    if (!rates) return;
    Object.entries(rates).forEach(([type, months]) => {
      if (BOND_RATES_HISTORY[type]) Object.assign(BOND_RATES_HISTORY[type], months);
    });
    localStorage.setItem(CACHE_KEY, JSON.stringify({ ts: Date.now(), rates }));
  } catch (e) {
    console.warn("Nie udało się pobrać aktualnych stawek:", e);
  }
}
